/**
 * Translates a non-English crop name to English
 * @param {string} name - The crop name in any supported language
 * @returns {string} - The English translation or original name if not found
 */
export const translateToEnglish = (name) => {
  if (!name) return '';
  
  const lowercaseName = name.toLowerCase().trim();
  
  // Check if already in English (basic check)
  if (/^[a-zA-Z\s]+$/.test(name)) {
    return lowercaseName;
  }
  
  // Look up in translation dictionary
  const translation = translationDict[name.trim()] || translationDict[lowercaseName];
  
  return translation || lowercaseName;
};

export default translationDict;
